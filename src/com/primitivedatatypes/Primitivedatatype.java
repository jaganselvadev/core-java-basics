package com.primitivedatatypes;

public class Primitivedatatype {
 
	public static void main(String[] args) {
		
		byte b= 13;
		short s= 15;
		int i=20;
		long l=40l;
		float f=3.44f;
		double d=8.90;
		
		System.out.println(b+s+i+l+f+d);
		
		char c='4';
		System.out.println(c);
		
		boolean bo=true;
		System.out.println(bo);
	}
}
